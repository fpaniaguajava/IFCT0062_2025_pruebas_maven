package it.pkg.util;

public class Calculadora {
    public static int sumar(int s1, int s2) {
        return s1 + s2;
    }
    public static int restar(int r1, int r2) {
        return r1 - r2;
    }
    public static int multiplicar(int m1, int m2) {
        return m1 * m2;
    }
    public static int dividir(int d1, int d2) {
        return d1 / d2;
    }
}
